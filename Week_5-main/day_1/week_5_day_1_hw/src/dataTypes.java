public class dataTypes {
    public static void main(String[] args) {
        boolean trueOrFalse;
        // A super compact version of int, would be good for age
        byte byteVariable;
        char aSingleLonelyLetter;
        //A bigger version of byte, but smaller than an int
        short shortyMcShorty;
        int number1;
        long zeLongestNumberEver;
        float myRootBeerFloatNumber;
        double prepareForTrouble;

        trueOrFalse = true;
        byteVariable = 20;
        aSingleLonelyLetter = 'Z';
        shortyMcShorty = 1337;
        number1 = 1000000;
        zeLongestNumberEver = 77777777777L;
        myRootBeerFloatNumber = 3.14F;
        prepareForTrouble = 1111111.22222222;

        System.out.println(trueOrFalse);
        System.out.println(byteVariable);
        System.out.println(aSingleLonelyLetter);
        System.out.println(shortyMcShorty);
        System.out.println(number1);
        System.out.println(zeLongestNumberEver);
        System.out.println(myRootBeerFloatNumber);
        System.out.println(prepareForTrouble);

    }
}
